public class Calculadora {

    /**
     * Esse método é utilizado para somar dois números inteiros
     * @param numeroUm esse é o primeiro parâmetro do método
     * @param numeroDois esse é o segundo parâmetro do método
     * @return int o resultado desse método é a soma dos dois parâmetros
     */
    
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }
}
